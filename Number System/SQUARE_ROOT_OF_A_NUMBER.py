a = 45
b = 78
c = 81

# METHOD-1
# Square root of a number --> given_number ** 0.5
# The printed value will be in float.
print(a ** 0.5)
print(b ** 0.5)
print(c ** 0.5)

# METHOD-2
# By the help of math module 
# math.sqrt(number)
# The printed value will be in float.

import math
print(math.sqrt(a))
print(math.sqrt(b))
print(math.sqrt(c))

